# Description: add calculator
# Params: number-1, number-2
def add(a, b):
  return a + b

# Description: subtract calculator
# Params: number-1, number-2
def subtract(x, y):
  return x - y

# Description: divide calculator
# Params: number-1, number-2
def divide(j, k):
  return j / k

# print results with custom values
print(add(1, 2))
print(subtract(4, 1))
print(divide(8, 2))
